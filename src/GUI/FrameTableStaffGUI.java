    private void btnSearchBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchBookActionPerformed
        try {
            dftStaff.setRowCount(0);
            ////// mỗi lần gọi lại xoá mịa cái table đi
            if (txtSearch.getText().isEmpty()) {
                // nếu txtfield rỗng thì hiển thị lại cái bảng đầy đủ
//                loadStaff(); // hàm đẩy từ csdl vào bảng //
                JOptionPane.showMessageDialog(null, "Vui lòng nhập vào nội dung !");
                loadStaff();
                return;
            }
            String s = txtSearch.getText().toLowerCase(); // chuyển chữ trong txtfield về chữ thường
            // nhờ v mà seach chữ thường chữ hoa gì cũng ra hết
            liststaff = null;
            liststaff = StaffDAL.getListStaff();
            dftStaff.setRowCount(0);
            for (StaffDTO nv : liststaff) {
                if (nv.getStaff_name().toLowerCase().contains(s)) {
                    // đúng điều kiện thì đẩy nó vào table
                    Vector<Object> vec = new Vector<Object>();
                    vec.add(nv.getStaff_id());
                    vec.add(nv.getStaff_name());
                    vec.add(nv.getBirthYear());
                    vec.add(nv.getGender());
                    vec.add(nv.getAddress());
                    vec.add(nv.getPhoneNumber());
                    dftStaff.addRow(vec);
                }
                // .DesignLai.dtm.addRow(vec);
            }
        } catch (Exception e) {
            // TODO: handle exception
        }
    }//GEN-LAST:event_btnSearchBookActionPerformed

    public void loadStaff() {
        liststaff = null;
        liststaff = StaffDAL.getListStaff();
        dftStaff.setRowCount(0);
        for (StaffDTO nv : liststaff) {
            Vector<Object> vec = new Vector<Object>();
            vec.add(nv.getStaff_id());
            vec.add(nv.getStaff_name());
            vec.add(nv.getBirthYear());
            vec.add(nv.getGender());
            vec.add(nv.getAddress());
            vec.add(nv.getPhoneNumber());
            dftStaff.addRow(vec);

        }
    }

