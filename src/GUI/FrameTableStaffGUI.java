    private void btnSaveStaffIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveStaffIdActionPerformed
        int i = tbStaff.getSelectedRow();
        if (i >= 0) {
            String staff_id = dftStaff.getValueAt(i, 0).toString();
            CouponGUI.txtStaffId.setText(staff_id);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Chưa chọn vào table");
        }
    }//GEN-LAST:event_btnSaveStaffIdActionPerformed
