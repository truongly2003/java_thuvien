    private void btnSaveNCCIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveNCCIdActionPerformed
        int i = tbNCC.getSelectedRow();
        if (i >= 0) {
            String supplier_id = dftSupplier.getValueAt(i, 0).toString();
            CouponGUI.txtSupplierId.setText(supplier_id);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Chưa chọn vào table");
        }

    }//GEN-LAST:event_btnSaveNCCIdActionPerformed

    private void btnSearchNCCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchNCCActionPerformed
        try {
            dftSupplier.setRowCount(0);
            if (txtSearchNCC.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập vào nội dung !");
                loadSupplier();
                return;
            }
            String s = txtSearchNCC.getText().toLowerCase();
            listsupplier = null;
            listsupplier = SupplierDAL.getListSupplier();
            dftSupplier.setRowCount(0);
            for (SupplierDTO supp : listsupplier) {
                if (supp.getSupplier_name().toLowerCase().contains(s)) {
                    // đúng điều kiện thì đẩy nó vào table
                    Vector<Object> vec = new Vector<Object>();
                    vec.add(supp.getSupplier_id());
                    vec.add(supp.getSupplier_name());
                    dftSupplier.addRow(vec);
                }
                // .DesignLai.dtm.addRow(vec);
            }
        } catch (Exception e) {
            // TODO: handle exception
        }
    }//GEN-LAST:event_btnSearchNCCActionPerformed

