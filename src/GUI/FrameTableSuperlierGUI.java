    public static ArrayList<SupplierDTO> listsupplier = new ArrayList<SupplierDTO>();

    public void loadSupplier() {
        listsupplier = null;
        listsupplier = SupplierDAL.getListSupplier();
        dftSupplier.setRowCount(0);
        for (SupplierDTO supplier : listsupplier) {
            Vector<Object> vec = new Vector<Object>();
            vec.add(supplier.getSupplier_id());

            vec.add(supplier.getSupplier_name());
            dftSupplier.addRow(vec);
        }

    }

